# Exemplo de manipulação de strings em Python
mensagem = "Olá, mundo!"
print("Mensagem original:", mensagem)

# Transformando para maiúsculas
mensagem_upper = mensagem.upper()
print("Mensagem em maiúsculas:", mensagem_upper)

# Substituindo parte da string
mensagem_substituida = mensagem.replace("mundo", "Python")
print("Mensagem substituída:", mensagem_substituida)

# Dividindo a string em uma lista de palavras
palavras = mensagem.split()
print("Lista de palavras:", palavras)
